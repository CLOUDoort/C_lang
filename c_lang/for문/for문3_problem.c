// 문제 3 / 1000 이하의 3 또는 5의 배수인 자연수들의 합을 구한다.
// 자력 해결


// 배수 값을 구할 때 조건식 설정 시 %를 잘 활용하자. 이를 제대로 활용할 줄 알아야 한다.
// 그리고 return 값은 제발 제일 마지막에 넣자. 엉뚱한 데 넣어서 자꾸 0 리턴하지 말고...
#include <stdio.h>
int main() {
	int i, sum;
	sum = 0;

	for (i = 0; i <= 1000; i++) {
		if (i % 3 == 0 || i % 5 == 0) {
			sum += i;
		}
	}
	printf("1000 이하의 3의 배수 또는 5의 배수인 자연수들의 합은 %d 이다.", sum);
	
	return 0;
}


